package com.huawei.registry.entity;

/**
 * Basic instance information definition
 *
 * @author zhouss
 * @since 2022-02-23
 */
public class SimpleInstanceInfo {
    /**
     * host
     */
    private String host;

    /**
     * Port
     */
    private int port;

    /**
     * Constructor
     *
     * @param host domain name
     * @param port Port
     */
    public SimpleInstanceInfo(String host, int port) {
        this.host = host;
        this.port = port;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
